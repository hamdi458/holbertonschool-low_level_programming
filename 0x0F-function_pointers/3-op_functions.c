#include "3-calc.h"
#include <stdio.h>
/**
 * op_add - fun add
 * @a: var
 * @b: var
 * Return: Always 0
 */
int op_add(int a, int b)
{
return (a + b);
}
/**
 * op_sub - fun sub
 * @a: var
 * @b: var
 * Return: Always 0
 */
int op_sub(int a, int b)
{
return (a - b);
}
/**
 * op_mul - fun sub
 * @a: var
 * @b: var
 * Return: Always 0
 */
int op_mul(int a, int b)
{
return (a * b);
}
/**
 * op_div - fun div
 * @a: var
 * @b: var
 *
 * Return: Always 0
 */
int op_div(int a, int b)
{
return (a / b);
}
/**
 * op_mod - fun mod
 * @a: var
 * @b: var
 * Return: Always 0
 */
int op_mod(int a, int b)
{
return (a % b);
}
